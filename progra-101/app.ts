
let weekDay: number = 1; ////lunes =1

switch(weekDay){
    case 1:
        console.log("Es lunes");
        break;

    case 2:
        console.log("Es martes");
        break;

    case 3:
        console.log("Es miercoles");
        break;

    default:
        console.log("Condicion por default")


}